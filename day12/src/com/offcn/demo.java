package com.offcn;

public class demo {
    //静态变量和 非静态变量(成员变量)的区别
    /*1.概念上,所属不同:
    非静态变量属于对象
    *静态变量属于 类 类变量
    *2.内存空间不足,存储位置不同
    *非静态变量属于对象,所以存储在堆内存中
    静态变量属于类  存储 在方法区的静态区中
    //内存时间不同 生命周期不同
       非静态变量属于对象,所以生命周期和对象相同,随着对象的创建而存在,随着对象的消失而消失
       静态变量属于类 所以生命周期和类相同 随着类的加载(创建对象.类名访问静态变量.类名访问静态方法
       ).反射的方式.加载子类.运行某个测试类)而存在,随着类的消失(内存管理)而消失;

    * */

}
