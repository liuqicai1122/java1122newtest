package com.offcn.demo;

public class Demo_String入门 {
    public static void main(String[] args) {
        /*String 就是字符串 字符串的表现形式就是被"引起来的内容"
        *它不是基本的数据类型   引用数据类型 就是复杂的数据
        *字符串的定义 和变量一样
        * 数据类型 变量名 =变量值
        * String name ="dadwa"
        * */
        String name ="  432234 ";
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println(name);
        System.out.println();
        System.out.println();
        System.out.println(name);
        //由此可知 System.out.println() 单独出来是空出来一行
    }
}
