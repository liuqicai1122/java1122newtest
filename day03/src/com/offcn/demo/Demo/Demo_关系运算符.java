package com.offcn.demo.Demo;

public class Demo_关系运算符 {
    public static void main(String[] args) {
        /*==  != 不等于 > <  >= <=
        *
        * */
        int x1 = 3;
        int x2 = 4;
        boolean x3 = x1 != x2;
        System.out.println(x3);
        boolean x4= x1 < x2;
        System.out.println(x4);
        boolean x5 = x1 <= x2;
        System.out.println(x5);
        boolean x6 = x1 >= x2;
        System.out.println(x6);


    }



}
